#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	x;

	x = (n < 0) ? n * -1 : n;
	if (n < 0)
		ft_putchar('-');
	if (x >= 10)
	{
		ft_putnbr(x / 10);
		ft_putnbr(x % 10);
	}
	else
		ft_putchar('0' + x);
}
