#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	x;

	x = (n < 0) ? n * -1 : n;
	if (n < 0)
		ft_putchar_fd('-', fd);
	if (x >= 10)
	{
		ft_putnbr_fd(x / 10, fd);
		ft_putnbr_fd(x % 10, fd);
	}
	else
		ft_putchar_fd('0' + x, fd);
}
